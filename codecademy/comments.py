# this function takes 
# one argument, num and 
# returns num squared
def square(num):
  numSquared = num * num
  return numSquared

# several test cases
# to verify the function
# is working as expected

print(square(2)) # Prints 4
print(square(5)) # Prints 25
print(square(8)) # Prints 64
print(square(10)) # Prints 100